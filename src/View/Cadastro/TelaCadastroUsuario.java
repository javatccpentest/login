    private void jLabel7MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MousePressed
        if (validacaoGeral()) {
            UsuarioJpaController JPA = new UsuarioJpaController(EntityManager.getEntityManagerFactory());
            Usuario user = new Usuario();

            try {

                user = preencherUsuario(user);
                JPA.create(user);

                new login().setVisible(true);
                this.dispose();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jLabel7MousePressed

