    private void validarLogin() {
        valLogin = valid.validarLogin(edtLogin.getText());
        if (valLogin == false) {
            edtLogin.setBackground(Color.red);
        } else {
            edtLogin.setBackground(Color.green);
        }
    }

    private void validarCpf() {
        valCpf = valid.validarCpf(edtCpf.getText());
        if (valCpf == false) {
            edtCpf.setBackground(Color.red);
        } else {
            edtCpf.setBackground(Color.green);
        }
    }

    private void validarNome() {
        if (edtNome.getText().length() < 2) {
            edtNome.setBackground(Color.red);
        } else {
            edtNome.setBackground(Color.green);
        }
    }

    private void validarNomeCompleto() {
        if (edtNomeCompleto.getText().length() < 5 && !edtNomeCompleto.getText().contains(" ")) {
            edtNomeCompleto.setBackground(Color.red);
        } else {
            edtNomeCompleto.setBackground(Color.green);
        }
    }

    private void validarSenha() {
        if (Arrays.toString(edtSenha.getPassword()).length() < 15) {
            edtSenha.setBackground(Color.red);
        } else {
            edtSenha.setBackground(Color.green);
        }
    }

    private Usuario preencherUsuario(Usuario user) {
        user.setNome(edtNome.getText());
        user.setLogin(edtLogin.getText());
        user.setCpf(edtCpf.getText());
        user.setEmail(edtEmail.getText());
        user.setNomeCompleto(edtNomeCompleto.getText());
        user.setSenha(String.copyValueOf(edtSenha.getPassword()));
        return user;
    }

