    private boolean validacaoGeral() {
        validarCpf();
        validarEmail();
        validarLogin();
        validarNome();
        validarNomeCompleto();
        validarSenha();
        if (valEmail && valCpf && valLogin
                && edtNome.getBackground().equals(Color.green)
                && edtNomeCompleto.getBackground().equals(Color.green)
                && edtSenha.getBackground().equals(Color.green)) {

            return true;

        }
        return false;
    }

